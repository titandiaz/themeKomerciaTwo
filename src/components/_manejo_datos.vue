6. DERECHOS DE LOS TITULARES DE LOS DATOS PERSONALES
Las personas naturales cuyos datos personales sean objeto de tratamiento por parte de
DISTRIBUCIONES TOPALXE LTDA, tienen los siguientes derechos, los cuales pueden ejercer
en cualquier momento:
6.1. Conocer los datos personales sobre los cuales DISTRIBUCIONES TOPALXE LTDA;
está realizando el tratamiento. De igual manera, el titular puede solicitar en
cualquier momento, que sus datos sean actualizados o rectificados por ejemplo,
si encuentra que sus datos son parciales, inexactos, incompletos, fraccionados,
induzcan a error, o aquellos cuyo tratamiento esté expresamente prohibido o no
haya sido autorizado.
6.2. Solicitar prueba de la autorización otorgada a DISTRIBUCIONES TOPALXE LTDA
para el tratamiento de sus datos personales.
6.3. Ser informado por DISTRIBUCIONES TOPALXE LTDA, previa solicitud, respecto del
uso que esta le dará a sus datos personales.
6.4. Presentar ante la Superintendencia de Industria y Comercio quejas por
infracciones a lo dispuesto en la ley de protección a datos personales.
6.5. Solicitar a la empresa la supresión de sus datos personales y/o revocar la
autorización otorgada para el tratamiento de los mismos, mediante la
presentación de un reclamo, de acuerdo con los procedimientos establecidos
en el numeral 9 de esta política. No obstante, la solicitud de supresión de la
información y la revocatoria de la autorización, no procederán cuando el titular
de la información tenga un deber legal o contractual de permanecer en la base
de datos y/o archivos, ni mientras se encuentre vigente la relación entre el titular
y DISTRIBUCIONES TOPALXE LTDA, en virtud de la cual fueron recolectados sus
datos.
6.6. Acceder de forma gratuita a sus datos personales objeto de tratamiento.
7. PERSONA O AREA RESPONSABLE DE LA IMPLEMENTACIÓN Y OBSERVACIÓN DE ESTA POLÍTICA
La gerencia Administrativa de DISTRIBUCIONES TOPALXE LTDA; tiene a su cargo la labor de
desarrollo, implementación, capacitación y observación de esta política, así como a
mantener actualizada la base de datos y dar respuesta a todas las peticiones, quejas y
reclamos que se reciban por parte de los titulares de datos personales.
La gerencia Administrativa es la responsable de la atención de peticiones, consultas,
quejas y reclamos, ante la cual, el titular de la información podrá ejercer sus derechos a
conocer, actualizar, rectificar y suprimir el dato y revocar la autorización.
8. AUTORIZACIÓN
DISTRIBUCIONES TOPALXE LTDA; debe solicitar autorización previa, expresa e informada a
los titulares de los datos personales sobre los que requiera realizar el tratamiento.
8.1. Autorización previa significa, que el consentimiento debe ser otorgado por el
titular, a más tardar en el momento de la recolección de los datos personales.
8.2. Autorización expresa quiere decir, que el consentimiento del titular debe ser
explícito y concreto, no son válidas las autorizaciones abiertas y no específicas.
Se requiere que el titular manifieste su voluntad de autorizar que DISTRIBUCIONES
TOPALXE LTDA., realice el tratamiento de sus datos personales.
Esta manifestación de voluntad del titular se dará a través del mecanismo puesto
a disposición por DISTRIBUCIONES TOPALXE LTDA:, el cual consiste en el
diligenciamiento de un formato de autorización ( ANEXO 1) , el cual se
conservará para que pueda ser consultada con posterioridad, o de forma oral
por ejemplo , en una conversación telefónica o en videoconferencia.
8.3. Autorización informada significa que al momento de solicitar el consentimiento
al titular, debe informársele claramente:
- Los datos personales que serán recolectados
- La identificación y datos de contacto del responsable y encargado del
tratamiento.
- Las finalidades específicas del tratamiento que se pretende realizar, es decir:
cómo y para qué se va a hacer la recolección, el uso, la circulación de los datos
personales.
- Cuáles son los derechos qué tiene como titular de los datos personales; para el
efecto ver el numeral 6 de esta política.
