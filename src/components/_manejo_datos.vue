datos personales, para el adecuado desarrollo de sus actividades comerciales, recolecta,
almacena, usa, circula y suprime datos personales correspondientes a personas naturales
y jurídicas, con quien tiene o ha tenido relación, tales como, sin que la enumeración
signifique limitación trabajadores, clientes, proveedores , acreedores y deudores.
