servicio; e) Realizar estudios internos sobre hábitos de consumo; f) Enviar al correo físico,
electrónico, celular o dispositivo móvil, vía mensajes de texto (SMS y/o MMS) o a través de
cualquier otro medio análogo y/o digital de comunicación creado o por crearse, información
comercial, publicitaria o promocional sobre los productos y/o servicios, eventos y/o
promociones de tipo comercial o no de estas, con el fin de impulsar, invitar, dirigir, ejecutar,
informar y de manera general, llevar a cabo campañas, promociones o concursos de
carácter comercial o publicitario, adelantados por DISTRIBUCIONES TOPALXE LTDA y/o por
