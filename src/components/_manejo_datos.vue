    NORMATIVIDAD LEGAL Y AMBITO DE APLICACIÓN:
La presente política de tratamiento de datos personales es elaborada de conformidad con
lo dispuesto en la Constitución Política , La ley 1581 de 2012, reglamentada por el decreto
1377 de 2013 el cual fue compilado en el decreto único 1074 de 2015 y demás disposiciones
complementarias y será aplicada a DISTRIBUCIONES TOPALXE LTDA, respecto de la
recolección , almacenamiento, uso , circulación, supresión y de todas aquellas actividades
que constituyan tratamiento de datos personales.
1. ALCANCE
Esta Política de protección de datos personales se aplicará a todas las bases de datos y/o
archivos que contengan datos personales que sean objeto de tratamiento por parte de
DISTRIBUCIONES TOPALXE LTDA encargada del tratamiento de datos personales.
